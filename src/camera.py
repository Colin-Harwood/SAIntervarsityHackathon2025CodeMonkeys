import cv2
#///////////////////////////////////////////////////////////////////////////////////////////////////////////////
#Check if there is a cam and try to open it
#///////////////////////////////////////////////////////////////////////////////////////////////////////////////
def check_camera():
    camera = cv2.VideoCapture(0)
    if (camera.isOpened() == False):
        print("Error: Cant open camera!")
        exit()
    return camera

#///////////////////////////////////////////////////////////////////////////////////////////////////////////////
#Get the frames from the camera so we can process the data
#///////////////////////////////////////////////////////////////////////////////////////////////////////////////
def get_camFrameData(camera):
    bReadFrame, arrFrames = camera.read()

    if not bReadFrame:
        print("Error: Cant read the frames!")
        return None
    else: 
        return arrFrames